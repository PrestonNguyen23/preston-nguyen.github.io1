function min(a, b) {
    // Use the ternary operator to return the minimum of the two numbers
    return a < b ? a : b;
  
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(n) {
// Handle negative numbers (not considered in this implementation)
// Take the absolute value of the number to handle negatives
const absN = Math.abs(n);

// Base cases
if (absN === 0) {
  return true; // Zero is even
} else if (absN === 1) {
  return false; // One is odd
} else {
  // Recursive case: Check evenness based on |N| - 2
  return isEven(absN - 2);
}
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(str, char) {
// Initialize count variable to store the number of occurrences
let count = 0;

// Iterate through each character in the string
for (let i = 0; i < str.length; i++) {
  // Check if the current character is equal to the specified character
  if (str[i] === char) {
    // Increment count if a match is found
    count++;
  }
}

// Return the final count
return count;
}

