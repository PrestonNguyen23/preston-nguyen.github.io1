////////////////////////////////////////////////////////////////////////////////

function fizzBuzz() {
  for (let i = 1; i <= 15; i++) {
    let output = '';
  
    if (i % 3 === 0) {
      output += 'fizz';
    }
  
    if (i % 5 === 0) {
      output += 'buzz';
    }
  
    console.log(output || i);
  }
}

////////////////////////////////////////////////////////////////////////////////
// drawChessboard //////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function drawChessboard(size) {
  let chessboard = '';

  for (let row = 1; row <= size; row++) {
    for (let col = 1; col <= size; col++) {
      if ((row + col) % 2 === 0) {
        chessboard += ' ';
      } else {
        chessboard += '#';
      }
    }
    chessboard += '\n';
  }

  console.log(chessboard);
}

