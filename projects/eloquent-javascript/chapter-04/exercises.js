////////////////////////////////////////////////////////////////////////////////
// range ///////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function range(start, end, step = 1) {
   // Check if start and end are the same or step is 0, return an empty array
   if (start === end || step === 0) {
    return [];
  }

  var result = [];

  if (step > 0) {
    for (var i = start; i <= end; i += step) {
      result.push(i);
    }
  } else {
    for (var i = start; i >= end; i += step) {
      result.push(i);
    }
  }

  return result;
  };


////////////////////////////////////////////////////////////////////////////////
// sum /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function sum(numbers) {
    return numbers.reduce(function(acc, num) {
      return acc + num;
    }, 0);
}

////////////////////////////////////////////////////////////////////////////////
// reverseArray ////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function reverseArray(arr) {
  var reversed = [];
  for (var i = arr.length - 1; i >= 0; i--) {
    reversed.push(arr[i]);
  }
  return reversed;
}

////////////////////////////////////////////////////////////////////////////////
// reverseArrayInPlace /////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function reverseArrayInPlace(arr) {
  var len = arr.length;
  for (var i = 0; i < Math.floor(len / 2); i++) {
    var temp = arr[i];
    arr[i] = arr[len - 1 - i];
    arr[len - 1 - i] = temp;
  }
}

////////////////////////////////////////////////////////////////////////////////
// arrayToList /////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function arrayToList(arr) {
  var list = null;
  for (var i = arr.length - 1; i >= 0; i--) {
    list = { value: arr[i], rest: list };
  }
  return list;
}

////////////////////////////////////////////////////////////////////////////////
// listToArray /////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function listToArray(list) {
  var arr = [];
  while (list !== null) {
    arr.push(list.value);
    list = list.rest;
  }
  return arr;
}

////////////////////////////////////////////////////////////////////////////////
// prepend /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function prepend(element, list) {
  return { value: element, rest: list };
}

////////////////////////////////////////////////////////////////////////////////
// nth /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function nth(list, position) {
  if (list === null) {
    return undefined;
  } else if (position === 0) {
    return list.value;
  } else {
    return nth(list.rest, position - 1);
  }
};


////////////////////////////////////////////////////////////////////////////////
// deepEqual ///////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function deepEqual(val1, val2) {
 // Check if the values are strictly equal
 if (val1 === val2) {
  return true;
}

// Check if either value is null or not an object
if (val1 == null || typeof val1 !== 'object' ||
  val2 == null || typeof val2 !== 'object') {
  return false;
}

// Get the keys of the objects
var keys1 = Object.keys(val1);
var keys2 = Object.keys(val2);

// Check if the number of keys is the same
if (keys1.length !== keys2.length) {
  return false;
}

// Check if the values of corresponding keys are deepEqual
for (var key of keys1) {
  if (!keys2.includes(key) || !deepEqual(val1[key], val2[key])) {
      return false;
  }
}

// If all checks pass, the values are deepEqual
return true;
}

